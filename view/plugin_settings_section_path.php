<p>Input values here to do string replacements</p>
<p>eg:
	<br />original paths http://test.com/wp-gallery/;http://test.com/wp-content/images/
	<br />target paths http://test.com/gallery/;http://test.com/images/
	<br />&nbsp;&nbsp;&nbsp;>> anything with http://test.com/wp-gallery/ will be replaced with http://test.com/gallery/
	<br />&nbsp;&nbsp;&nbsp;>> anything with http://test.com/wp-content/images/ will be replaced with http://test.com/images/
</p>
<p>Useful when you have apache alias or symbolic links</p>
<p><strong>Leave blank for none</strong></p>